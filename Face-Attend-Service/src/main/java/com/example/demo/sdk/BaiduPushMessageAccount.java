package com.example.demo.sdk;

public class BaiduPushMessageAccount {

	private String id;
	private String deviceId;
	private String baiduChannelId;
	private String baiduUserId;
	
	/**
	 * @return the id
	 */
	public String getId() {
		return id;
	}
	/**
	 * @param id the id to set
	 */
	public void setId(String id) {
		this.id = id;
	}
	/**
	 * @return the deviceId
	 */
	public String getDeviceId() {
		return deviceId;
	}
	/**
	 * @param deviceId the deviceId to set
	 */
	public void setDeviceId(String deviceId) {
		this.deviceId = deviceId;
	}
	/**
	 * @return the baiduChannelId
	 */
	public String getBaiduChannelId() {
		return baiduChannelId;
	}
	/**
	 * @param baiduChannelId the baiduChannelId to set
	 */
	public void setBaiduChannelId(String baiduChannelId) {
		this.baiduChannelId = baiduChannelId;
	}
	/**
	 * @return the baiduUserId
	 */
	public String getBaiduUserId() {
		return baiduUserId;
	}
	/**
	 * @param baiduUserId the baiduUserId to set
	 */
	public void setBaiduUserId(String baiduUserId) {
		this.baiduUserId = baiduUserId;
	}
}